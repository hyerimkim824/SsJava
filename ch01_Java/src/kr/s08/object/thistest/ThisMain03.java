package kr.s08.object.thistest;

public class ThisMain03 {
	
	private int korean;
	private int enlish;
	private int math;
	private int history;
	private int science;
	private int music;
	private int art;
	
	//실수할 가능성이 있기 때문에 method를 표준적으로 자동생성하는 것이 좋다.
	public int getKorean() {
		return korean;
	}
	public void setKorean(int korean) {
		this.korean = korean;
	}
	public int getEnlish() {
		return enlish;
	}
	public void setEnlish(int enlish) {
		this.enlish = enlish;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getHistory() {
		return history;
	}
	public void setHistory(int history) {
		this.history = history;
	}
	public int getScience() {
		return science;
	}
	public void setScience(int science) {
		this.science = science;
	}
	public int getMusic() {
		return music;
	}
	public void setMusic(int music) {
		this.music = music;
	}
	public int getArt() {
		return art;
	}
	public void setArt(int art) {
		this.art = art;
	}
	
	
	

}
