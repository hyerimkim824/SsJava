package kr.s10.object.extention;

public class Phone {
	
	//같은 패키지 또는 상속관계일 경우 접근 허용
	protected String number;
	protected String model;
	protected String color;
	
	public String getNumber() {
		return number;
	}
	public String getModel() {
		return model;
	}
	
	public String getColor() {
		return color;
	}
	

}
